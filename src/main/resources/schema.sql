DROP TABLE IF EXISTS RECIPE_TYPE;

CREATE TABLE RECIPE_TYPE
(
    ID INTEGER ,
    NAME VARCHAR(255) NOT NULL

);

DROP TABLE IF EXISTS RECIPE;

CREATE TABLE RECIPE
(
    ID INTEGER ,
    NAME VARCHAR(255) NOT NULL,
    RECIPE_TYPE_ID INTEGER NOT NULL

);

DROP TABLE IF EXISTS INGREDIENT;

CREATE TABLE INGREDIENT
(
    ID INTEGER ,
    NAME VARCHAR(255) NOT NULL

);